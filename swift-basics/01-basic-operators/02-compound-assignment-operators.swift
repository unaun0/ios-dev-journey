/*
Составные Операторы присваивания
*/

// Как и в C, в Swift есть операторы составного присваивания, которые объединяют присваивание (=) с другой операцией. 
// Один из примеров — оператор присваивания со сложением (+=)

var a = 1
a += 2 // 3

// Операторы составного присваивания не возвращают значение. Например, нельзя написать let b = a += 2.